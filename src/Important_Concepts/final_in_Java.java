package Important_Concepts;

public class final_in_Java {
    public static void main(String[] args) {
        final int a = 34;

        System.out.println("The Value of a is :- " + a);
        // The Value of a is :- 34

        // a = 50;   <---- Cannot change value of const variable.
    }
}


